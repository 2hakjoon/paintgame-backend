let usedWord = [];

export const unlockWord = (word) =>{
    usedWord.map((wd,index)=>{
        if(wd === word){
            usedWord.splice(index,1);
        }
    })
}

export const rndWord = () => {
    let word = "";
    let check = true;
    while(check){
        word = chooseWord();
        check = usedWord.includes(word)
    }
    return word;
}

